int		ft_isalnum(int ch)
{
	int i;

	i = 0;
	if (ch >= 'a' && ch <= 'z')
		i = 1;
	if (ch >= 'A' && ch <= 'Z')
		i = 1;
	if (ch >= '0' && ch <= '9')
		i = 1;
	if (i == 0)
		return (0);
	else
		return (1);
}
