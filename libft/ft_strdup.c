#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *str)
{
	size_t		i;
	size_t		len;
	char		*dup;

	len = ft_strlen(str);
	dup = (char*)malloc(sizeof(char) * (len + 1));
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
